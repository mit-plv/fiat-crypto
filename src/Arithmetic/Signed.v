(* This file proves the doubling case is never reached during point multiplication when using
signed limbs to encode the number. I.e. k*P where k is encoded in w-sized signed limbs.

We assume a limb size w or a generic base B, and a modulus N (the curve's group order).

This is a formal version of the corresponding proof in boringssl:
https://boringssl.googlesource.com/boringssl/+/refs/heads/main/crypto/fipsmodule/ec/util.cc.inc
The main differences to boringssl are:
- We do not assume our number is generated by any specific recoding algorithm, rather we look at an
  arbitrary number represented with signed limbs of a certain bound.
- Our proof is arithmetic instead of using bitwise logic, that is why it looks quite different. *)

(* Side Note: If you want to do multiplication with a group order or limb size that do not
fit these criteria, there is an alternative: You can chop off an additional bit by checking
if your number k is larger than N/2, and if it is, calculate k*P = (N-k)(-P) instead.
The lemmas in this file should be partially fit for the approach, but would need some adaptation. *)

Require Import Coq.ZArith.ZArith Lia.
Require Import Crypto.Util.ZUtil.Modulo.
#[local] Open Scope Z_scope.

Lemma bounded_no_doubling N B (lo hi : Z)
  (n_large : B < N) (n_mod_B_large : B <= 2*(N mod B))
  (lo_bounded : -B + 2 <= 2*lo <= B) (hi_bounded : 0 < lo + B * hi < N)
  : (B * hi) mod N <> lo mod N.
Proof.
  rewrite Z.cong_iff_0, Z.mod_divide by lia; intros [q].
  assert (q = 1) by (assert (q = 0 \/ q = 1) by nia; assert (hi <> 0); nia); subst q.
  assert ((2*N - hi*(2*B)) mod (2*B) <= B - 2) by (rewrite Z.mod_small; lia).
  rewrite <-Zminus_mod_idemp_r, Z_mod_mult, Z.sub_0_r, Zmult_mod_distr_l in *. lia.
Qed.

Lemma bounded_no_doubling' N w (lo hi : Z)
  (w_pos : 0 < w) (n_large : 2^w < N) (n_mod_2w_large : 2^(w-1) <= N mod 2^w)
  (lo_bounded : -2^w + 2 <= 2*lo <= 2^w) (hi_bounded : 0 < lo + 2^w * hi < N)
  : (2^w * hi) mod N <> lo mod N.
Proof.
  assert (2^w = 2*2^(w-1)) by (rewrite <-(Z.succ_pred w), Z.pow_succ_r, <-Z.sub_1_r at 1; lia).
  apply bounded_no_doubling; lia.
Qed.

Import List.

Section PositionalWithBase.
Context B (positional := fold_right (fun a s => a + (B)*s) 0).

Lemma positional_0_inv_app xs ys
  (xs_bounds : Forall (fun x => -B + 2 <= 2*x <= B) xs)
  (ys_bounds : Forall (fun x => -B + 2 <= 2*x <= B) ys)
  (positional_bound : positional (xs++ys) = 0) : positional xs = 0 /\ positional ys = 0.
Proof.
  induction xs_bounds as [|x xs], ys_bounds as [|y ys];
    cbn [app fold_right positional] in *; rewrite ?app_nil_r in *; try lia.
  case (Z.eqb_spec (positional (xs ++ y :: ys)) 0); nia.
Qed.

Lemma bounded_positional_inv_app N xs ys
  (xs_bounds : Forall (fun x => -B + 2 <= 2*x <= B) xs)
  (ys_bounds : Forall (fun x => -B + 2 <= 2*x <= B) ys)
  (positional_bound : 0 < positional (xs++ys) < N) : 0 <= positional ys < N.
Proof.
  induction xs_bounds as [|x xs], ys_bounds as [|y ys];
    cbn [app fold_right positional] in *; rewrite ?app_nil_r in *; try lia.
  case (Z.eqb_spec (positional (xs ++ y :: ys)) 0) as [e|]; [|nia].
  repeat match goal with H : positional (_++_) = 0 |- _ =>
    apply positional_0_inv_app in H as []; eauto using Forall_cons; [] end.
  cbn [positional fold_right] in *. lia.
Qed.

Lemma positional_cons h t :
  positional (h :: t) = h + B*(positional t).
Proof. reflexivity. Qed.

Lemma fixed_window_no_doubling N x xs ys
  (n_large : B < N) (n_mod_B_large : B <= 2*(N mod B))
  (xs_bounds : Forall (fun x => -B + 2 <= 2*x <= B) xs)
  (ys_bounds : Forall (fun x => -B + 2 <= 2*x <= B) ys)
  (x_bounds : -B + 2 <= 2*x <= B)
  (non_zero : ~ (x = 0 /\ positional ys = 0))
  (positional_bound : 0 < positional (xs++x::ys) < N) :
  B * positional ys mod N <> x mod N.
Proof.
  apply bounded_no_doubling; try assumption.

  pose proof (bounded_positional_inv_app N xs (x :: ys)) as [? ?]; [|apply Forall_cons| | ]; try assumption.
  rewrite <-positional_cons. split; [apply Z.le_neq|]; try split; try lia.

  intros Hxys.
  pose proof (positional_0_inv_app (x::nil) ys) as [? ?]; try apply Forall_cons; try apply Forall_nil;  try assumption. symmetry; cbn; assumption.
  apply non_zero; cbn in *; lia.
Qed.

End PositionalWithBase.

Section PositionalWithW.
Context w (positional := fold_right (fun a s => a + (2^w)*s) 0).

#[local] Notation B := (2^w).

Lemma fixed_window_no_doubling' N x xs ys
  (w_pos : 0 < w)
  (n_large : B < N) (n_mod_2w_large : 2^(w-1) <= N mod 2^w)
  (xs_bounds : Forall (fun x => -B + 2 <= 2*x <= B) xs)
  (ys_bounds : Forall (fun x => -B + 2 <= 2*x <= B) ys)
  (x_bounds : -B + 2 <= 2*x <= B)
  (non_zero : ~ (x = 0 /\ positional ys = 0))
  (positional_bound : 0 < positional (xs++x::ys) < N) :
  B * positional ys mod N <> x mod N.
Proof.
  assert (2^w = 2*2^(w-1)) by (rewrite <-(Z.succ_pred w), Z.pow_succ_r, <-Z.sub_1_r at 1; lia).
  apply fixed_window_no_doubling with (xs:=xs); try assumption. lia.
Qed.

End PositionalWithW.
